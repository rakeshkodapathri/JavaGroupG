package com.backend.codes.FlightManagementSystemGroupG;

public class Passenger {
    private String fullName;
    private int age;
    private String phone;
    private String email;
    private String passportNum;
    private String  gender;

    // Getters and Setters
    public String getFullName() { return fullName; }
    public void setFullName(String flightNumber) { this.fullName = flightNumber; }

    public int getAge() { return age; }
    public void setAge(int age) { this.age = age; }

    public String getPhone() { return phone; }
    public void setPhone(String phone) { this.phone = phone; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getPassportNum() { return passportNum; }
    public void setPassportNum(String passportNum) { this.passportNum = passportNum; }

    public String getGender() { return gender; }
    public void setGender(String gender) { this.gender = gender; }


}
